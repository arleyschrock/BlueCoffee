namespace Microsoft.Experimental.Azure.JavaPlatform.Log4j
{
	/// <summary>
	/// The trace level for Log4j logging.
	/// </summary>
	public enum Log4jTraceLevel
	{
		/// <summary>
		/// Debug-only messages.
		/// </summary>
		DEBUG,
		/// <summary>
		/// Trace-level messages.
		/// </summary>
		TRACE,
		/// <summary>
		/// Info-level messages.
		/// </summary>
		INFO,
		/// <summary>
		/// Warning messages.
		/// </summary>
		WARN,
		/// <summary>
		/// Error messages.
		/// </summary>
		ERROR,
	};
}
